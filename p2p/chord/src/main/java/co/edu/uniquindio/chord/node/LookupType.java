package co.edu.uniquindio.chord.node;

/**
 * The {@code LookupType} enumeration identifies the purpose of why it is
 * performing a look up
 * 
 * @author Daniel Pelaez
 * @author Hector Hurtado
 * @author Daniel Lopez
 * @version 1.0, 17/06/2010
 * @since 1.0
 * @see ChordNode
 */
public enum LookupType {

	/**
	 * Lookup for fingers table stabilized
	 */
	FINGERS_TABLE,

	/**
	 * Lookup for user
	 */
	LOOKUP,

	/**
	 * Lookup for node join
	 */
	JOIN,

	/**
	 * Other lookup
	 */
	OTHER;
}
